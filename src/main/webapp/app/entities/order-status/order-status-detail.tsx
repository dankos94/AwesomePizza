import React, { useEffect } from 'react';
import { Link, useParams } from 'react-router-dom';
import { Button, Col, Row } from 'reactstrap';
import { Translate } from 'react-jhipster';
import { FontAwesomeIcon } from '@fortawesome/react-fontawesome';

import { useAppDispatch, useAppSelector } from 'app/config/store';

import { getEntity } from './order-status.reducer';

export const OrderStatusDetail = () => {
  const dispatch = useAppDispatch();

  const { id } = useParams<'id'>();

  useEffect(() => {
    dispatch(getEntity(id));
  }, []);

  const orderStatusEntity = useAppSelector(state => state.orderStatus.entity);
  return (
    <Row>
      <Col md="8">
        <h2 data-cy="orderStatusDetailsHeading">
          <Translate contentKey="awesomePizzaApp.orderStatus.detail.title">OrderStatus</Translate>
        </h2>
        <dl className="jh-entity-details">
          <dt>
            <span id="id">
              <Translate contentKey="global.field.id">ID</Translate>
            </span>
          </dt>
          <dd>{orderStatusEntity.id}</dd>
          <dt>
            <span id="statusName">
              <Translate contentKey="awesomePizzaApp.orderStatus.statusName">Status Name</Translate>
            </span>
          </dt>
          <dd>{orderStatusEntity.statusName}</dd>
        </dl>
        <Button tag={Link} to="/order-status" replace color="info" data-cy="entityDetailsBackButton">
          <FontAwesomeIcon icon="arrow-left" />{' '}
          <span className="d-none d-md-inline">
            <Translate contentKey="entity.action.back">Back</Translate>
          </span>
        </Button>
        &nbsp;
        <Button tag={Link} to={`/order-status/${orderStatusEntity.id}/edit`} replace color="primary">
          <FontAwesomeIcon icon="pencil-alt" />{' '}
          <span className="d-none d-md-inline">
            <Translate contentKey="entity.action.edit">Edit</Translate>
          </span>
        </Button>
      </Col>
    </Row>
  );
};

export default OrderStatusDetail;
